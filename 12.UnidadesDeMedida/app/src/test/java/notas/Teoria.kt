                        UNIDADES DE MEDIDA

                PIXEL (px)
    Un pixel es la menor unidad homgenea en color que forma parte de una imagen digital
    No se recomienda utilizar esta medida ya que no todos los dispositivos tienen la misma dendisad de pixeles
    por ello no en todos los dispositivos se vera igual esta medida


                DENSIDAD DE PIXELES
    Es la cantidad de pixeles que cabe en una pulgada.
    Las pantalls de alta densidad tienen mas pixeles por pulgada que los de baja densidad


                PIXELES DE DENSIDAD INDEPENDIENTE (dp or dip)

    Son unidades felixibles que se escalan a dimensiones uniformes en cualquier pantalla.
    Un dp es igual a un pixel en una pantalla de densidad de 160.
    Para calcular dp:
    dp = (ancho en pixeles * 160) / densidad de la pantalla


                PIXELES DE ESCALA INDEPENDIENTE (sp)

    tienen lamisma funcion que los dp, pero se utilizan para las fuentes.
    El valor por defecto de un sp es el mismo que el valor por defecto de un dp.
    El tamano cambia dependiendo de como tenga configurado el usuario el tamano de la fuente en su celular


                DIFERENCIA ENTRE UN SP Y UN DP
    El sp preserva los ajustes del usuario respecto a la fuente.
    Los usuarios que tengan ajustes par agrandar el texto veran que el tamano del texto concuerda con sus preferencias.


                    LAS SIGUIENTES MEDIDAS NO SE RECOMIENDAN MUCHO

    mm (Milimetros)
    Son milimetros reales segun el tamano fisico de la pantalla

    in (pulgadas)
    Son pulgadas reales segun el tamano fisico de la pantalla

    pt (points)
    Es un 172 de una pulgada, segun el tamano fisico de la pantalla






