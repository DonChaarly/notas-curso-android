                    GENERAR ARCHIVO APK

    Los APK (Android Application Package) son archivos que se utilizan para distribuir e instalar las aplicaciones
    en dispositivos que utilizan android
    Este archivo tambien se necesita enviar a googlePlay para publicar la apliccion

    Para generar el apk se necesita
    1. En el manifest se necesitan indicar las siguientes etiquetas:
        android:versionCode = "1"
            Esta etiqueta se utiliza para que el desarrollador gestione las versiones que va sacando
            y valdra 1, 2, 3... dependiendo de cuantas versiones se van sacando
        android:versionName = "1.0
            Esta etiqueta se utiliza para especificar la version que podra consultar el suuario
            y valdra 1.0, 1.1, 1.2, 2.0... dependiendo de cuantas versiones se van sacando

    2. En la pestana de Build se selecciona
        Build Bundle(s) / APK(s) -> Build APK(s)
    3. Se generara un archivo apk llamado app-debug.apk en la carpeta de app > build > outputs > apk >debug
        Este es el archivo apk en el que queda comprimido el codigo y toda la aplicacion
        Con ingeneria inversa se puede obtener el codigo a partir del archivo apk (Esto se utiliza para hackeo)



                    REGISTRO LEGAL DE LA APP (Reomiendo ver el video por cuestion de dudas)

